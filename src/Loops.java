public class Loops {
    public static void main(String[] args) {
        // Loops

        // For
        for (var i = 0; i < 5; i++){
            System.out.println("Usando o FOR");
        }

        // While
        int i = 0;
        while (i < 5){
            i++;
            System.out.println("Imprimindo o while");
        }
    }
}
