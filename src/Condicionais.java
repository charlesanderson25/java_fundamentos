public class Condicionais {
    public static void main(String[] args) {
        // Condicionais

        byte idade = 25;

        if (idade > 26){
            System.out.println("Executa IF ");
        } else if (idade == 25) {
            System.out.println("Executa Else IF");
        } else {
            System.out.println("Executa ELSE");
        }
    }
}
